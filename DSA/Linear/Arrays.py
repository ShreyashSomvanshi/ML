# An array is a collection of items stored at contiguous memory locations. It can store multiple items of the same type.

# Creating an array
arr = [1, 2, 3, 4, 5]

# Accessing elements
print(arr[0])  # Output: 1

# Modifying elements
arr[1] = 10
print(arr)  # Output: [1, 10, 3, 4, 5]

# Iterating through an array
for num in arr:
    print(num)
